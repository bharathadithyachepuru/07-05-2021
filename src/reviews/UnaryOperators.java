package reviews;

public class UnaryOperators {

	public static void main(String[] args) {
		
		int a=2, b=4, c=6;
		
		//post increment
		
		int d = a++;
		
		//pre increment
		
		int e = ++c;
		
		//post decrement
		
		int f = c--;
		
		//pre decrement
		
		int g = --a;
		
		System.out.println(d);
		
		System.out.println(e);
		
		System.out.println(f);
		
		System.out.println(g);
		
		
	}

}
