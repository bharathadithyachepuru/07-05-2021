package reviews;

public class WideningTypeCasting {

	public static void main(String[] args) {
		
		int a = 7;  
		  
		long b = a;  
		
		float c = b;  
		
		System.out.println("Before converting int value is "+a);  
		System.out.println("After converting long value is "+b);  
		System.out.println("After converting float value is "+c);  

	}

}
