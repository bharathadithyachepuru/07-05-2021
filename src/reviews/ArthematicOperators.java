package reviews;

public class ArthematicOperators {

	public static void main(String[] args) {

		int a = 5, b =15, c = 45;
		
		//Addition
		
		System.out.println(a+b);
		
		//Subtraction
		
		System.out.println(c-b);
		
		//Multiplication
		
		System.out.println(a*b);
		
		//Division (Coefficient is output)
		
		System.out.println(c/b);
		
		//Mode (Remainder is output)
		
		System.out.println(c%b);
		
		
	}

}
